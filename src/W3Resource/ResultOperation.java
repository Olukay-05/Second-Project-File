package W3Resource;

public class ResultOperation {

    public static void main(String[] args) {

        int num1 = -5;
        int num2 = 8;
        int num3 = 6;

        int evaluation = num1 + num2 * num3;

        int num4 = 55;
        int num5 = 9;

        double result = (num4 + num5) % num5;

        int number1 = 20;
        int number2 = -3;
        int number3 = 5;
        int number4 = 8;

        int solution = number1 + number2 * number3 / number4;

        int figure = 15;
        int figure2 = 3;
        int figure3 = 2;

        int total = number3 + figure / figure2 * figure3 - number4 % figure2;

        System.out.println(evaluation);
        System.out.println(result);
        System.out.println(solution);
        System.out.println(total);

    }
}
