public class ComputingAnExpression {

    public static void main(String[] args) {

        double number1 = 25.5;
        double number2 = 3.5;
        double number3 = 40.5;
        double number4 = 4.5;

        double SolutionToEquation = ((number1 * number2 - number2 * number2) / (number3 - number4));

        System.out.println(SolutionToEquation);
    }
}
